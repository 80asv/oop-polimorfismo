


public class Cuadrado extends Figura2D{
    private int lado;
    private int lado1;

    public Cuadrado(int lado, int lado1) {
        this.lado = lado;
        this.lado1 = lado1;
    }

    public int getLado() {
        return lado;
    }

    public void setLado(int lado) {
        this.lado = lado;
    }

    public int getLado1() {
        return lado1;
    }

    public void setLado1(int lado1) {
        this.lado1 = lado1;
    }
}

    
        
    
    
  
